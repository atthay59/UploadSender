package com.grokonez.excelfile.model;

public class AllowList {
	
	private String sid;
	
	private String listSenderName;
	
	private String remark;
 
	public AllowList() {
	}


	public String getSid() {
		return sid;
	}


	public void setSid(String sid) {
		this.sid = sid;
	}


	public String getListSenderName() {
		return listSenderName;
	}


	public void setListSenderName(String listSenderName) {
		this.listSenderName = listSenderName;
	}


	public String getRemark() {
		return remark;
	}


	public void setRemark(String remark) {
		this.remark = remark;
	}
 
}